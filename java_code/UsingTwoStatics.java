
class UsingTwoStatics
{
public static void ste1()
{
  System.out.println("DEADMAN");
 }

public static void ste2()
 {
  System.out.println("TAKER");
 }

public static void main(String args[])
 {
int a=5, b=6;
int sum = a + b;
System.out.println("Sum is: " + sum);
System.out.println("Hello World");
ste1();
ste2();
  }
}
